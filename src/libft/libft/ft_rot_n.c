char	*ft_rot_n(char *str, int n)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if (str[i] <= 90 && str[i] >= 65)
		{
			str[i] = str[i] - 65;
			str[i] = ((str[i] + n) % 26);
			str[i] = str[i] + 65;
		}
		else if (str[i] <= 122 && str[i] >= 97)
		{
			str[i] = str[i] - 97;
			str[i] = ((str[i] + n) % 26);
			str[i] = str[i] + 97;
		}
		i++;
	}
	return (str);
}
